    private void recordsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recordsActionPerformed
        try{
        java.io.File file = new java.io.File("rentals.txt"); 
        if(!file.exists()){
        JOptionPane.showMessageDialog(this, "No rental recors found.");
        return; 
        }
        java.util.Scanner scanner = new java.util.Scanner(file);
        StringBuilder records = new StringBuilder();
        
        while(scanner.hasNextLine()){
        records.append(scanner.nextLine()).append("\n");
        }
        scanner.close(); 
        if(records.length() == 0){
        JOptionPane.showMessageDialog(this, "No rental records found.");
        }else{
        JOptionPane.showMessageDialog(this, records.toString());
        }
        }catch(java.io.FileNotFoundException e){
        JOptionPane.showMessageDialog(this, "Filer not found "+ e.getMessage());
        }
    }//GEN-LAST:event_recordsActionPerformed

