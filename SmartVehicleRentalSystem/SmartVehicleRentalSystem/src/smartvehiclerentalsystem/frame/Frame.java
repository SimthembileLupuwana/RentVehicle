    private void rentACarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rentACarActionPerformed
        String customerName;

        do {
            customerName = JOptionPane.showInputDialog(this, "Customer Name: ");
            if (customerName == null) {
                return;
            }
            if (customerName.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Customer name cannot be empty. Please try again.");
            }
        } while (customerName.trim().isEmpty());

        String registrationNumber;

        do {
            registrationNumber = JOptionPane.showInputDialog(this, "Registration Number: ");
            if (registrationNumber == null) {
                return;
            }
            if (registrationNumber.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Registration number cannot be empty. Please try again.");
            }
        } while (registrationNumber.trim().isEmpty());

        String brand;
        do {
            brand = JOptionPane.showInputDialog(this, "Brand: ");
            if (brand == null) {
                return;
            }
            if (brand.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Brand cannot be empty. Please try again.");
            }
        } while (brand.trim().isEmpty());

        String model;
        do {
            model = JOptionPane.showInputDialog(this, "Model: ");
            if (model == null) {
                return;
            }
            if (model.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Model cannot be empty. Please try again.");
            }
        } while (model.trim().isEmpty());
        String dailyRateInput;
        do {
            dailyRateInput = JOptionPane.showInputDialog(this, "Daily Rate: ");
            if (dailyRateInput == null) {
                return;
            }
            if (dailyRateInput.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Daily Rate cannot be empty. Please try again.");
            }
        } while (dailyRateInput.trim().isEmpty());
        String insuranceFeeInput;
        do {
            insuranceFeeInput = JOptionPane.showInputDialog(this, "Insurance Fee:");
            if (insuranceFeeInput == null) {
                return;
            }
            if (insuranceFeeInput.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Insurance fee cannot be empty. Please try again.");
            }
        } while (insuranceFeeInput.trim().isEmpty());
        String daysInput;
        do {
            daysInput = JOptionPane.showInputDialog(this, "Days: ");
            if (daysInput == null) {
                return;
            }
            if (daysInput.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Days cannot be empty. Please try again.");
            }
        } while (daysInput.trim().isEmpty());
        try {
            double dailyRate = Double.parseDouble(dailyRateInput.trim());
            double insuranceFee = Double.parseDouble(insuranceFeeInput.trim());
            int days = Integer.parseInt(daysInput.trim());
            if (days <= 0) {
                throw new InvalidRentalDaysException("Rental days must be greater than 0.");
            }

            Car car = new Car(insuranceFee, registrationNumber, brand, model, dailyRate);
            double cost = car.calculateRentalCost(days);

            java.io.FileWriter fw = new java.io.FileWriter("rentals.txt", true);
            fw.write("Customer: " + customerName + " | Type: Car | Reg: " + registrationNumber + " | Days: " + days + " | Total Cost: R" + cost + "\n");
            fw.close();

            JOptionPane.showMessageDialog(this, "Car rented successfully!\nTotal Cost: R" + cost);

        } catch (InvalidRentalDaysException e) {
            JOptionPane.showMessageDialog(this, "Invalid days: " + e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid input! Numbers only for rate, fee and days.");
        } catch (java.io.IOException e) {
            JOptionPane.showMessageDialog(this, "File error: " + e.getMessage());
        }
    }//GEN-LAST:event_rentACarActionPerformed

